
public class TiposPrimitivos {
	
	public static void main(String[] args) {
		
		String variavelString = ""; //Não tem tipo primitivo.
		
		byte numeroByte = 1; //Armazena de -127 até 127
		
		short numeroShort = 1; //Armazena de -32768 até 32767
		
		int numeroInteger = 1; //Armazena de -2147483648 até 2147483647
		
		long numeroLong = 1L; //Armazena de -9,223372037x10elevadoa 18 até 9,223372037x10elevadoa 18
		
		float numeroFloat = 1.0f;
		
		double numeroDouble = 1.0;
		
		char variavelCharacter = 'A';
		
		boolean variavelBoolean = true;
		
		/*O tipo normal pode está em um estado de ausência de valor, como usado em OO, 
		basta aplicar o valor "null" que atribui nenhum valor a variável. Os tipos primitivos
		não aceitam o valor null(não aceitam valores nulos)
		*/
	}

}
