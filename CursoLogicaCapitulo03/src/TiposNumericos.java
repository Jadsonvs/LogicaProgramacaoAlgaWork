
public class TiposNumericos {
	
	public static void main(String[] args) {
		
		Byte numeroByte = 1; //Armazena de -127 até 127
		
		Short numeroShort = 1; //Armazena de -32768 até 32767
		
		Integer numeroInteger = 1; //Armazena de -2147483648 até 2147483647
		
		Long numeroLong = 1L; //Armazena de -9,223372037x10elevadoa 18 até 9,223372037x10elevadoa 18
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0;
	}

}
